#1.  Escreva um programa que conte de 1 a 10 e imprima cada número.

numero = 1  # Inicializa a variável numero com o valor 1

while numero <= 10:  # Inicia um loop while que continuará enquanto o numero for menor ou igual a 10
    print(numero)  # Imprime o valor atual de numero
    numero += 1  # Incrementa o valor de numero em 1 em cada iteração do loop

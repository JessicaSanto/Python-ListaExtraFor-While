#Escreva um programa que imprima os números pares de 1 a 20.

numero = 2  # Inicializa a variável numero com o valor 2

while numero <= 20:  # Inicia um loop while que continuará enquanto o numero for menor ou igual a 20
    print(numero)  # Imprime o valor atual de numero
    numero += 2  # Incrementa o valor de numero em 2 em cada iteração do loop, garantindo que permaneça par
